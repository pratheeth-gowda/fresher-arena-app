class Job {
  final int id;
  final String role;
  final String company;
  final String location;
  final String salary;
  final String type;
  final String description;

  const Job({
    required this.id,
    required this.role,
    required this.company,
    required this.location,
    required this.salary,
    required this.type,
    required this.description,
  });
}